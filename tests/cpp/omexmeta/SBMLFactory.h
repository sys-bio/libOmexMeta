class SBMLFactory {
public:
    static std::string getSBML(ModelType modelType) {
        if (modelType == SBML_ANNOTATED)
            return SBMLAnnotated().str();
        else if (modelType == SBML_NOT_ANNOTATED)
            return SBMLNotAnnotated().str();
        else if (modelType == SBML_NOT_ANNOTATED2)
            return SBMLNotAnnotated2().str();
        else if (modelType == SBML_SEMANTIC_EXTRACTION_MODEL)
            return SBMLSemanticExtractionModel().str();
        else if (modelType == SBML_BIOMD204)
            return BIOMD204().str();
        else if (modelType == SBML_BIOMD366)
            return BIOMD366().str();
        else if (modelType == SBML_BIOMD626)
            return BIOMD626().str();
        else if (modelType == SBML_BIOMD689)
            return BIOMD689().str();
        else if (modelType == SBML_WITH_BAG)
            return SBMLWithBag().str();
        else if (modelType == SBML_ADHMODEL)
            return ADHModel().str();
        else if (modelType == SBML_NERNST_POTENTIAL)
            return NernstPotential().str();
        else if (modelType == SBML_NERNST_POTENTIAL2)
            return NernstPotential2().str();
        else if (modelType == SBML_INVALID_METAIDS)
            return InvalidMetaids().str();
        else {
            throw std::invalid_argument("SBMLFactory error: ModelType is not a valid argument.");
        }
    }
};


